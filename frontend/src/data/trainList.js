// Search trains by name or number
export const searchTrains = (query) => {
  if (!query) return trains;
  
  const searchTerm = query.toLowerCase().trim();
  return trains.filter(train => 
    train.name.toLowerCase().includes(searchTerm) || 
    train.number.includes(searchTerm)
  );
};

// Get unique train names (without duplicates)
export const getUniqueTrainNames = () => {
  const uniqueNames = new Set();
  return trains.filter(train => {
    if (uniqueNames.has(train.name)) {
      return false;
    }
    uniqueNames.add(train.name);
    return true;
  });
};
