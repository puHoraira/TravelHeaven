/**
 * Search stations by name (English or Bengali)
 * @param {string} query - Search query
 * @returns {Array} Matching stations
 */
export const searchStations = (query) => {
  if (!query || query.trim().length === 0) {
    return [];
  }

  const searchTerm = query.toLowerCase().trim();
  
  return railwayStations.filter(station => 
    station.en.toLowerCase().includes(searchTerm) ||
    station.bn.includes(searchTerm) ||
    station.district.toLowerCase().includes(searchTerm) ||
    (station.code && station.code.toLowerCase().includes(searchTerm))
  ).slice(0, 10); // Limit to 10 results
};

/**
 * Get station by exact name
 * @param {string} name - Station name
 * @returns {Object|null} Station object or null
 */
export const getStationByName = (name) => {
  return railwayStations.find(station => 
    station.en.toLowerCase() === name.toLowerCase()
  ) || null;
};
